
public class Discussions {
	
	private String dTitle;
	private String dTag;
	
	public Discussions(String dTitle, String dTag) {
		this.dTitle = dTitle;
		this.dTag = dTag;
		
	}
	
	public String getdTitle() {
		return dTitle;
	}

	public void setdTitle(String dTitle) {
		this.dTitle = dTitle;
	}
	
	public String getdTag() {
		return dTag;
	}

	public void setdTag(String dTag) {
		this.dTag = dTag;
	}

}
